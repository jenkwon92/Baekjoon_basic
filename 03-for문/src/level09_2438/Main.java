package level09_2438;

import java.util.Scanner;

public class Main {
	public static void main(String[] args) {
		//첫째 줄에는 별 1개, 둘째 줄에는 별 2개, N번째 줄에는 별 N개를 찍는 문제
		
		Scanner sc = new Scanner(System.in);
		int N = sc.nextInt();
	
		for(int i=1; i<=N; i++) {
			for(int a=1; a<=i; a++) {
				System.out.print("*");
			}
			System.out.print("\n");
		}
		
		sc.close();
	}
}
